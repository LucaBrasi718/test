#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int count;

	count = 10;
	if (n == -2147483648)
	{
		ft_putchar_fd('-', fd);
		ft_putchar_fd('2', fd);
		n = 147483648;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n >= 10)
		ft_putnbr_fd(n / count, fd);
	ft_putchar_fd((n % 10) + 48, fd);
}
