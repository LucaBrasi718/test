#include "libft.h"

void	ft_putnbr(int n)
{
	int	count;

	count = 10;
	if (n == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		n = 147483648;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n >= 10)
		ft_putnbr(n / count);
	ft_putchar((n % 10) + 48);
}
